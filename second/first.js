//	Learn About Operators in JavaScript, Types of Operators

//	............................................Arithmetic Operators...........................................................

//The addition operator (+) adds numbers:
let a = 10
let b = 20
let c = a + b

console.log(c);

//The subtraction operator (-) subtracts numbers.
let d = 10
let e = 5
let f = d - e

console.log(f);

//The multiplication operator (*) multiplies numbers.
let g = 10
let h = 20
let i = g * h

console.log(i);

//The division operator (/) divides numbers.
let j = 10
let k = 20
let l = j / k

console.log(l);

//The modulus operator (%) returns the division remainder.
let m = 3
let n = 2
let o = m % n

console.log(o);

//The increment operator (++) increments numbers.
let p = 10      //let p = 10
p++             //
let q = p       //let q = ++p


console.log(q)

//The decrement operator (--) increments numbers.
let r = 10      //let r = 10
r--             //
let s = r       //let s = --r


console.log(s)

//The exponentiation operator (**) raises the first operand to the power of the second operand.
let t = 15

let u = t ** 2

console.log(u)
//x ** y produces the same result as Math.pow(x,y):

let v=15

let w=Math.pow(v,2)

console.log (w)


